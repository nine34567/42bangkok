int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	while (str[i])
	{
		if (str[i] == '\0')
		{
			break ;
		}
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			a = 0;
			break ;
		}
		i++;
	}
	return (a);
}
