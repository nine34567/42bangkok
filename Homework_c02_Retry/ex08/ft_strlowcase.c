char	*ft_strlowcase(char *str)
{
	int		i;
	char	cur;

	i = 0;
	while (str[i])
	{
		cur = str[i];
		if (cur == '\0')
		{
			break ;
		}
		if (cur >= 'A' && cur <= 'Z')
		{
			str[i] = cur + 32;
		}
		i++;
	}
	return (str);
}
